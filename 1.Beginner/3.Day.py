#Day 3:Basic Syntax & Variables (Variables, data types (int, float, str, bool), naming rules, and simple printing)

#Variables
my_name = "Mohamed Dahir"
my_age = 22
my_height = 1.85
is_student = True

#Printing
print("Name:", my_name)
print("Age:", my_age)
print("Height:", my_height)
print("Is Student:", is_student)

#Formatted string
print(f"{my_name} is {my_age} years old, {my_height}cm tall, and student status is {is_student}.") 

#Using type() to check data types
print("Data Types:")
print("my_name:", type(my_name))
print("my_age:", type(my_age))
print("my_height:", type(my_height))
print("is_student:", type(is_student))

